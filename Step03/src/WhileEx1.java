
public class WhileEx1 {
	/*
	 * 	while(조건식){
	 * 		반복할 코드1
	 * 		반복할 코드2
	 * 		반복할 코드3
	 * 		...
	 * 	} 
	 * 	while문은 조건식이 true면 {실행한 영역} 코드를 반복을 한다.
	 * 	조건식이 false면 반복을 멈춘다.
	 */
	public static void main(String[] args) {
		int i = 5;//초기값
		
		while(i!=0) {//조건식
			System.out.println("Hello World");
			i--;//증감식
		}
	}

}







