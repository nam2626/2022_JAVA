
public class PrintString {
	/*
	 * 주석 코드에 설명문을 작성하는 영역 실행이 안되는 코드
	 */
	public static void main(String[] args) {
		// 한줄 단위 주석
		System.out.println("안녕하세요");
		System.out.println("Hello World - 1");
		System.out.println("Hello World - 2");
//		System.out.println("Hello World - 3");
		/*
		 * System.out.println("Hello World - 4"); System.out.println("Hello World - 5");
		 */
//		"문자열" + "문자열" ---> "문자열문자열"   하나의 문자열로 만듬
		System.out.println("안녕하세요 " + "자바입니다.");
		System.out.println(10);
		//문자열과 덧셈 연산을 하면 다른 값도 전부 문자열로 변환 후 하나의 문자열로 만듬
		System.out.println(20 + "Hello"); 
		System.out.println(30);
		System.out.println(40);
	}

}








